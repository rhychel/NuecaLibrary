package net.nueca.imonggo.enums;

public enum DatabaseOperations {
	/**
	 * Create table.
	 */
	CREATE,
	/**
	 * Insert new row on the table.
	 */
	INSERT,
	/**
	 * Delete a row on the table.
	 */
	DELETE,
	/**
	 * Delete all rows on the table.
	 */
	DELETEALL,
	/**
	 * Update a row on the table.
	 */
	UPDATE,
	/**
	 * Drop the table.
	 */
	DROP
}
